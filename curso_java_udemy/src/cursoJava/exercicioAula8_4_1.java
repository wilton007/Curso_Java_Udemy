package cursoJava;

public class exercicioAula8_4_1 {
    double valorDollar, quantDollar;

    public double converte(double valorDollar, double quantDollar) {
        double total = (6.0 / 100.0) * (valorDollar * quantDollar);
        total = total + (valorDollar * quantDollar);
        return total;
    }
}
